type User struct {
	Id         int       `orm:"column(uid);auto" description:"用户id"`
	DeviceId   string    `orm:"column(device_id);size(32);null" description:"用户设备id"`
	Uname      string    `orm:"column(uname);size(20);null" description:"用户名id"`
	passwd     string    `orm:"column(passwd);size(45);null" description:"用户密码id"`
	NickName   string    `orm:"column(nick_name);size(45);null" description:"用户昵称id"`
	FaviconUrl string    `orm:"column(favicon_url);size(45);null" description:"用户头像链接"`
	Phone      string    `orm:"column(phone);size(20);null" description:"用户手机号码"`
	Sex        string    `orm:"column(sex);null" description:"用户性别"`
	Birthday   time.Time `orm:"column(birthday);type(date);null" description:"用户生日"`
	RequestIp  string    `orm:"column(request_ip);size(15);null" description:"用户请求ip"`
	Location   string    `orm:"column(location);size(10);null" description:"用户区域"`
	Points     int       `orm:"column(points);null" description:"用户积分"`
	Coins int      		 `orm:"column(coins);null" description:"用户夺宝币"`
}

func (t *User) TableName() string {
	return "user"
}

func init() {
	orm.RegisterModel(new(User))
}

// AddUser insert a new User into database and returns
// last inserted Id on success.
func AddUser(m *User) (id int64, err error) {
	o := orm.NewOrm()
	id, err = o.Insert(m)
	return
}

// GetUserById retrieves User by Id. Returns error if
// Id doesn't exist
func GetUserById(id int) (v *User, err error) {
	o := orm.NewOrm()
	v = &User{Id: id}
	if err = o.Read(v); err == nil {
		return v, nil
	}
	return nil, err
}

// GetUserById retrieves User by Id. Returns error if
// Id doesn't exist
func GetUserByDeviceId(deviceId string) (v *User, err error) {
	var users []User
	o := orm.NewOrm()
	//vs = &User{DeviceId: deviceId}
	_,err = o.QueryTable(new(User)).Filter("device_id", deviceId).All(&users);
	if err == nil && len(users)>0 {
		v:= &users[0]
		return v, nil
	}
	return nil, err
}

// GetAllUser retrieves all User matches certain condition. Returns empty list if
// no records exist
func GetAllUser(query map[string]string, fields []string, sortby []string, order []string,
	offset int64, limit int64) (ml []interface{}, err error) {
	o := orm.NewOrm()
	qs := o.QueryTable(new(User))
	// query k=v
	for k, v := range query {
		// rewrite dot-notation to Object__Attribute
		k = strings.Replace(k, ".", "__", -1)
		qs = qs.Filter(k, v)
	}
	// order by:
	var sortFields []string
	if len(sortby) != 0 {
		if len(sortby) == len(order) {
			// 1) for each sort field, there is an associated order
			for i, v := range sortby {
				orderby := ""
				if order[i] == "desc" {
					orderby = "-" + v
				} else if order[i] == "asc" {
					orderby = v
				} else {
					return nil, errors.New("Error: Invalid order. Must be either [asc|desc]")
				}
				sortFields = append(sortFields, orderby)
			}
			qs = qs.OrderBy(sortFields...)
		} else if len(sortby) != len(order) && len(order) == 1 {
			// 2) there is exactly one order, all the sorted fields will be sorted by this order
			for _, v := range sortby {
				orderby := ""
				if order[0] == "desc" {
					orderby = "-" + v
				} else if order[0] == "asc" {
					orderby = v
				} else {
					return nil, errors.New("Error: Invalid order. Must be either [asc|desc]")
				}
				sortFields = append(sortFields, orderby)
			}
		} else if len(sortby) != len(order) && len(order) != 1 {
			return nil, errors.New("Error: 'sortby', 'order' sizes mismatch or 'order' size is not 1")
		}
	} else {
		if len(order) != 0 {
			return nil, errors.New("Error: unused 'order' fields")
		}
	}

	var l []User
	qs = qs.OrderBy(sortFields...)
	if _, err := qs.Limit(limit, offset).All(&l, fields...); err == nil {
		if len(fields) == 0 {
			for _, v := range l {
				ml = append(ml, v)
			}
		} else {
			// trim unused fields
			for _, v := range l {
				m := make(map[string]interface{})
				val := reflect.ValueOf(v)
				for _, fname := range fields {
					m[fname] = val.FieldByName(fname).Interface()
				}
				ml = append(ml, m)
			}
		}
		return ml, nil
	}
	return nil, err
}

